    private boolean verifyInput() {
        return wordleGame.getFiveLetterWords().contains(userInputField.getText());
    }

    //creates a new Wordle instance, clears the letters, colors, and resets the guessCount
    private void newGame() throws IOException {
        wordleGame.createWord();
        clearLetters();
        clearColors();
        guessCount = 1;
    }

    private void projectLetterColor(int[] letterColors) {
        //iterates through the array checking what color the letter should be
        for (int i = 0; i < letterColors.length; i++) {
            //case for each color
            switch (letterColors[i]) {
                //GREY = incorrect
                case 0:
                        letters[guessCount - 1][i].setBackground(Color.GRAY);
                    break;
                //YELLOW = Correct letter, wrong location
                case 1:
                        letters[guessCount - 1][i].setBackground(Color.YELLOW);
                    break;
                //GREEN = correct letter and location
                case 2:
                        letters[guessCount - 1][i].setBackground(Color.GREEN);
                    break;
            }
        }
    }

    //puts user input on the JLabels row coresponding to the guess number
    private void projectUserGuess() {
        String[] userGuessLetters = userInputField.getText().split("");
        for (int i = 0; i < 5; i++) {
            letters[guessCount - 1][i].setText(userGuessLetters[i]);
        }
    }

    //sets all JLabel letters to empty string
    private void clearLetters() {
        for (int row = 0; row < letters.length; row++) {
            for (int col = 0; col < letters[row].length; col++) {
                letters[row][col].setText("");
            }
        }
    }
    
    private void clearColors() {
        for (int row = 0; row < letters.length; row++) {
            for (int col = 0; col < letters[row].length; col++) {
                letters[row][col].setBackground(Color.WHITE);
            }
        }
    }

